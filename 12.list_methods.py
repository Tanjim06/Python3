l1 = [8,1,55,55,6,93,14,16,6,18,6]
print(l1)

l1.sort()        #sort the list
print(l1)

count = l1.count(6)      #counts how many six's are there
print(count)

l1.reverse()
print(l1)

l1.append(90)      #appends 90 last of the list
print(l1)

l1.insert(3,8)     #insert 8 at 3 index
print(l1)

l1.pop(1)
print(l1)      #delete the value of index 1

l1.remove(93)   #remove 93 from the list
print(l1)